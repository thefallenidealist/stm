/*************************************************************************************************
				nRF_enable_CRC()
*************************************************************************************************/
void nRF_enable_CRC(nRF_hw_t *nRF0)
{
	// Forced high if one of the bits in the EN_AA is high
	reg_tmp[EN_CRC] = 1;
	write_reg(nRF0, REG_CONFIG);
}

/*************************************************************************************************
				nRF_disable_CRC()
*************************************************************************************************/
void nRF_disable_CRC(nRF_hw_t *nRF0)
{
	reg_tmp[EN_CRC] = 0;
	write_reg(nRF0, REG_CONFIG);
}
