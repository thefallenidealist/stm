/*************************************************************************************************
				nRF_clear_bits()
*************************************************************************************************/
void nRF_clear_bits(nRF_hw_t *nRF0)	
{
	reg_tmp[RX_DR] = 1;	
	reg_tmp[TX_DS] = 1;
	reg_tmp[MAX_RT] = 1;
	write_reg(nRF0, REG_STATUS);
}
